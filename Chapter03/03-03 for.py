# for문의 응용
marks=[90,25,67,45,80]
number=0
for mark in marks :
  number=number+1
  if mark >= 60 :
      print("%d번 학생은 합격입니다." % number)
  else :
      print("%d번 학생은 불합격입니다." % number)

# for문과 continue문
# for문 안의 문장을 수행하는 도중 continue문을 만나면 for문의 처음으로 돌아가게 된다
marks=[90,25,67,45,80]
number=0
for mark in marks :
  number=number+1
  if mark < 60 :
      continue
  print("%d번 학생 축하합니다. 합격입니다." % number)

