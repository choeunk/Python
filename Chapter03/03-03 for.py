# 리스트 컴프리헨션 사용하기
a=[1,2,3,4]
result=[]
for num in a :
  result.append(num*3)
print(result)

# 바로 위 코딩을 간단하게
a=[1,2,3,4]
result=[num*3 for num in a]
print(result)

# 바로 위 코딩에서 [1,2,3,4] 중에서 짝수에만 3을 곱하여 담고 싶다면
a=[1,2,3,4]
result=[num*3 for num in a if num%2 == 0]
print(result)

